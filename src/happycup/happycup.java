    private boolean isvalidInput() {
        return (radiChocolate.isSelected() || radioStrawberry.isSelected() || radioToffe.isSelected() || radioVanilla.isSelected());
    }

    private void fillItem() {
        double TAXRATE = .06;
        this.order[this._currentorder] = new Item();
        if (this.radiChocolate.isSelected()) {
            this.order[this._currentorder]._type = "Chocolate";
        } else if (this.radioStrawberry.isSelected()) {
            this.order[this._currentorder]._type = "Straberry";
        } else if (this.radioVanilla.isSelected()) {
            this.order[this._currentorder]._type = "Vanilla";
        } else if (this.radioToffe.isSelected()) {
            this.order[this._currentorder]._type = "Toffe";
        }
        this.order[this._currentorder]._almonds = chAlmonds.isSelected();
        this.order[this._currentorder]._lemon = chLemon.isSelected();
        this.order[this._currentorder]._mint = chMint.isSelected();
        this.order[this._currentorder]._nutella = chNutella.isSelected();
        this.order[this._currentorder]._peach = chPeach.isSelected();
        this.order[this._currentorder]._peanut = chPeanut.isSelected();
        this.order[this._currentorder]._pumpkin = chPumpkine.isSelected();
        if (this.cNoTax.isSelected()) {
            this.order[this._currentorder]._tax = 0;
            this.order[this._currentorder]._totalwithouttax = this._currenttotal;
            this.order[this._currentorder]._totalwithtax = this._currenttotal;
        } else {
            this.order[this._currentorder]._tax = this._currenttotal * TAXRATE;
            this.order[this._currentorder]._totalwithouttax = this._currenttotal;
            this.order[this._currentorder]._totalwithtax = this._currenttotal + (this._currenttotal * TAXRATE);
        }

    }

    private void fillForm() {
        if (this.order[this._currentorder] != null) {
            if (this.order[this._currentorder]._type == "Chocolate") {
                this.radiChocolate.setSelected(true);
            } else if (this.order[this._currentorder]._type == "Straberry") {
                this.radioStrawberry.setSelected(true);
            } else if (this.order[this._currentorder]._type == "Vanilla") {
                this.radioVanilla.setSelected(true);
            } else if (this.order[this._currentorder]._type == "Toffe") {
                this.radioToffe.setSelected(true);
            }
            chAlmonds.setSelected(this.order[this._currentorder]._almonds);
            chLemon.setSelected(this.order[this._currentorder]._lemon);
            chMint.setSelected(this.order[this._currentorder]._mint);
            chNutella.setSelected(this.order[this._currentorder]._nutella);
            chPeach.setSelected(this.order[this._currentorder]._peach);
            chPeanut.setSelected(this.order[this._currentorder]._peanut);
            chPumpkine.setSelected(this.order[this._currentorder]._pumpkin);

            if (this.order[this._currentorder]._tax > 0) {
                this.cNoTax.setSelected(true);
                txtSubTotal.setText(String.valueOf(this.order[this._currentorder]._totalwithouttax));
                txtTax.setText(String.valueOf(this.order[this._currentorder]._tax));
                txtTotal.setText(String.valueOf(this.order[this._currentorder]._totalwithtax));

            } else {
                this.cNoTax.setSelected(false);
                txtSubTotal.setText(String.valueOf(this.order[this._currentorder]._totalwithouttax));
                txtTax.setText(String.valueOf(this.order[this._currentorder]._tax));
                txtTotal.setText(String.valueOf(this.order[this._currentorder]._totalwithtax));
            }
        }
        this.lblCurrent.setText(String.valueOf(this._currentorder + 1));
    }
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if (this._currentorder + 2 == this._ordercount) {
            if (this.isvalidInput()) {
                this.btnFinish.setEnabled(true);
                this.btnNext.setEnabled(false);
                btnBack.setEnabled(true);

                this.fillItem();
                this.resetForm();

                this._currentorder++;
                this.fillForm();
            } else {
                JOptionPane.showMessageDialog(this, "Please select Coffee type", "No type selected", JOptionPane.ERROR_MESSAGE);
                return;
            }

        } else {

            if (this.isvalidInput()) {
                this.btnFinish.setEnabled(false);

                this.fillItem();
                this.resetForm();
                this._currentorder++;
                if (this._currentorder == 0) {
                    btnBack.setEnabled(false);
                } else {
                    btnBack.setEnabled(true);
                }
                this.fillForm();
            } else {
                JOptionPane.showMessageDialog(this, "Please select Coffee type", "No type selected", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }


    }//GEN-LAST:event_btnNextActionPerformed
    private void recalculate() {
        double total;
        double subtotal = 0;
        double TAXRATE = .06;
        double tax;

        if (cNoTax.isSelected()) {
            TAXRATE = 0;
        }
        if (radioVanilla.isSelected()) {
            subtotal = subtotal + 8;
        } else if (radiChocolate.isSelected()) {
            subtotal = subtotal + 9;
        } else if (radioStrawberry.isSelected()) {
            subtotal = subtotal + 8;
        } else if (radioToffe.isSelected()) {
            subtotal = subtotal + 10;
        }

        if (chAlmonds.isSelected()) {
            subtotal = subtotal + 2;
        }
        if (chPeanut.isSelected()) {
            subtotal = subtotal + 1.5;
        }
        if (chMint.isSelected()) {
            subtotal = subtotal + 0.5;
        }
        if (chPeach.isSelected()) {
            subtotal = subtotal + 3;
        }
        if (chLemon.isSelected()) {
            subtotal = subtotal + 2;
        }
        if (chPumpkine.isSelected()) {
            subtotal = subtotal + 2.5;
        }
        if (chNutella.isSelected()) {
            subtotal = subtotal + 4;
        }

        txtSubTotal.setText(Double.toString(subtotal));

        subtotal = Double.parseDouble(txtSubTotal.getText());
        this._currenttotal = subtotal;
        tax = (subtotal * TAXRATE);
        DecimalFormat df = new DecimalFormat("#.##");
        tax = Double.valueOf(df.format(tax));
        total = tax + subtotal;

        txtTax.setText(Double.toString(tax));
        txtTotal.setText(Double.toString(total));
    }
