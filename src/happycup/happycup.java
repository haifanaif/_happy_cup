    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        if (this.isvalidInput()) {

            this.fillItem();

        } else {
            JOptionPane.showMessageDialog(this, "Please select Coffee type", "No type selected", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (int i = 0; i < this._ordercount; i++) {
            this.OrderTotal += order[i]._totalwithtax;
        }

        MainScreen ms = new MainScreen(this._ordercount, this.OrderTotal, this);
        this.setVisible(false);
        ms.setVisible(true);
    }//GEN-LAST:event_btnFinishActionPerformed
